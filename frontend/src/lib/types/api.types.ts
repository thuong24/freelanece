// ============================================================
// GLOBAL TYPESCRIPT TYPES — mirror backend API responses
// ============================================================

export interface ApiResponse<T> {
  success: boolean;
  message: string;
  data: T;
  meta?: PaginationMeta;
}

export interface PaginationMeta {
  page: number;
  limit: number;
  total: number;
  totalPages: number;
}

export interface ApiError {
  success: false;
  message: string;
  error: { code: string; details?: unknown };
}
